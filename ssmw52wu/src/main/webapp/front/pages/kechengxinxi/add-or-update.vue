			},
																														
																																																															
															// 下拉变化
			kemuChange(e) {
				this.kemuIndex = e.target.value
				this.ruleForm.kemu = this.kemuOptions[this.kemuIndex]
			},
																																																						
																																													kechengshipinTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.kechengshipin = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																								kechengtupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.kechengtupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
