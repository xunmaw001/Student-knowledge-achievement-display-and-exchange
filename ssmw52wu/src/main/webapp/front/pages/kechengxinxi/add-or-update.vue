					this.ruleForm.faburiqi = obj[o];
					this.ro.faburiqi = true;
					continue;
					}
					if(o=='kechengtupian'){
					this.ruleForm.kechengtupian = obj[o];
					this.ro.kechengtupian = true;
					continue;
					}
